<section id='prayer_points' class="s-shaded">

    <div class="row section-header" data-aos="fade-up">
        <div class="col-full">
            <h3 class="subhead logo-left">Step 6</h3>
            <h1 class="display-1">Organize Prayer Points</h1>
        </div>
    </div> <!-- end section-header -->

    <div class="row" data-aos="fade-up">
        <div class="col-full">
            <p>Pray4Tunisia creates 365 new prayer points throughout every year. This is because we value that our prayer requests reflect current events, stories, and prayer needs.</p>
            <p><strong>Here are some ideas for creating an ongoing system for creating and organizing your prayer points:</strong></p>
            <ul>
                <li>Brainstorming prayer point ideas
                    <ul>
                        <li>Create an ongoing brainstorm and content meeting for fresh ideas</li>
                        <li>Ask volunteers and church planters to submit prayer point ideas via an online form</li>
                        <li>Download Pray4Tunisia&#8217;s <a href="resources/Pray4Tunisia_prayer_requests.xls" target="_blank" rel="noopener"><span style="color: #16a1fd;">365 example prayer points</span></a> for ideas</li>
                        <li>Seek ideas for your content and network by studying what other networks are doing</li>
                    </ul>
                </li>
                <li>Writing prayer points
                    <ul>
                        <li>Ensure that your prayer points will not cause anyone harm. Pray4Tunisia does not include real names and photos in any prayer points.</li>
                        <li>Keep your network&#8217;s objective and vision in mind when writing prayer points</li>
                        <li>Be concise, about 1-3 lines.</li>
                        <li>Vary content (pictures, maps, stats, testimonies, videos, praises, petitions)</li>
                        <li>Proofread!</li>
                    </ul>
                </li>
                <li>Organizing prayer points
                    <ul>
                        <li>Consider using an online collaborative spreadsheet like <a href="https://www.google.com/sheets/about/" target="_blank" rel="noopener"><span style="color: #16a1fd;">Google Sheets</span></a> to store your prayer requests
                            <ul>
                                <li>It automatically saves all changes to the cloud that is reflective for every user</li>
                                <li>You can limit viewing and editing access</li>
                            </ul>
                        </li>
                    </ul>
                </li>
            </ul>
        </div>
    </div> <!-- end about-desc -->

